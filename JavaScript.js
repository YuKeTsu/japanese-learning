const stats = { correct: 0, incorrect: 0, total: 0 };

function updateStatsDisplay() {
  if (stats.total === 0) { $.stats.innerText = ""; return; }
  const pct = Math.round((stats.correct / stats.total) * 100);
  $.stats.innerText = `✅ 正确：${stats.correct}  |  ❌ 错误：${stats.incorrect}  |  🎯 正确率：${pct}%`;
}

function resetStats() {
  stats.correct = stats.incorrect = stats.total = 0;
  $.stats.innerText = "";
}


