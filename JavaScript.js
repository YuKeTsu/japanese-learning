function updateSelectedText() {
  const checkboxes = document.querySelectorAll('.lesson-chk');
  let checkedCount = 0;
  let selectedLabels = [];

  checkboxes.forEach(chk => {
    if (chk.checked) {
      checkedCount++;
      selectedLabels.push(chk.parentElement.textContent.trim());
    }
  });

  if (checkedCount === checkboxes.length) {
    $.selectAll.checked = true;
    $.multiselectHeader.innerText = "所有选项 (已全选)";
  } else if (checkedCount === 0) {
    $.selectAll.checked = false;
    $.multiselectHeader.innerText = "请选课";
  } else {
    $.selectAll.checked = false;
    $.multiselectHeader.innerText = `已选 ${checkedCount} 项: ` + selectedLabels.join(', ');
  }
}

