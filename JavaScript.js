// ========== 中文答案匹配 ==========
function getCnCandidates(cn) {
  const candidates = new Set();
  const add = s => {
    const v = s.trim();
    if (!v) return;
    candidates.add(v);
    const noTilde = v.replace(/[〜~]/g, '').trim();
    if (noTilde) candidates.add(noTilde);
  };

  add(cn);
  const noParen = cn.replace(/[（(][^）)]*[）)]/g, '').trim();
  if (noParen) add(noParen);

  cn.split(/[、，,]/).forEach(p => {
    const part = p.trim();
    if (!part) return;
    add(part);
    const cleaned = part.replace(/[（(][^）)]*[）)]/g, '').trim();
    if (cleaned) add(cleaned);
    const merged = part.replace(/[（(]([^）)]*)[）)]/g, '$1');
    if (merged && merged !== cleaned) add(merged);
  });

  return [...candidates];
}

function matchCnAnswer(userInput, cn) {
  const candidates = getCnCandidates(cn);
  return candidates.includes(userInput.replace(/[〜~]/g, '').trim());
}


