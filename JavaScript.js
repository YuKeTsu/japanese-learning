const StaticWordEngine = {
  wordQueue: [],
  questionCount: 0,
  totalInRound: 0,

  init() {
    let words = [];
    const checkboxes = document.querySelectorAll('.lesson-chk');
    let hasSelection = false;

    checkboxes.forEach(chk => {
      if (chk.checked) {
        hasSelection = true;
        if (lessons[chk.value]) {
          words = words.concat(lessons[chk.value]);
        }
      }
    });

    if (!hasSelection) return false;

    this.wordQueue = words.map(w => ({...w}));
    this.totalInRound = this.wordQueue.length;
    this.questionCount = 0;
    resetStats();

    if ($.randomMode.checked) {
      for (let i = this.wordQueue.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [this.wordQueue[i], this.wordQueue[j]] = [this.wordQueue[j], this.wordQueue[i]];
      }
    }
    return true;
  },

  next() {
    if (this.wordQueue.length === 0) return false;
    state.question = this.wordQueue.shift();
    this.questionCount++;

    $.progress.innerText = `本轮进度：${this.questionCount} / ${this.totalInRound}`;

    if ($.reverseMode.checked) {
      $.question.innerText = state.question.kanji || state.question.jp;
      $.kanjiDisplay.innerText = state.question.kanji
        ? "假名：" + state.question.jp : "";
    } else {
      $.question.innerText = "单词：" + state.question.cn;
      $.kanjiDisplay.innerText = $.showKanji.checked
        ? "汉字：" + (state.question.kanji || "无") : "";
    }
    return true;
  },

  check(userAnswer) {
    if ($.reverseMode.checked) {
      return matchCnAnswer(userAnswer, state.question.cn);
    }
    return userAnswer === state.question.jp;
  }
};

const DynamicExerciseEngine = {
  questionCount: 0,
  currentLevel: 0,
  digitLevels: [
    { label: "个位/十位", max: 20 },
    { label: "百以内", max: 100 },
    { label: "千以内", max: 1000 },
    { label: "万以内", max: 10000 },
    { label: "十万以内", max: 100000 },
    { label: "百万以内", max: 1000000 },
    { label: "千万以内", max: 10000000 },
    { label: "亿以内", max: 100000000 },
    { label: "九亿以内挑战", max: 1000000000 }
  ],

  _roundOver: false,

  init() {
    this.questionCount = 0;
    this.currentLevel = 0;
    this._roundOver = false;
    resetStats();
    return true;
  },

  next() {
    if (this._roundOver) {
      this._roundOver = false;
      return false;
    }
    const levelData = this.digitLevels[this.currentLevel];
    const min = this.currentLevel === 0 ? 0 : this.digitLevels[this.currentLevel - 1].max;
    const max = levelData.max;
    const randomVal = Math.floor(Math.random() * (max - min)) + min;

    this.questionCount++;

    state.question = {
      jp: this.numberToJapanese(randomVal),
      cn: randomVal.toLocaleString(),
      kanji: "当前难度：" + levelData.label
    };

    if ($.exerciseReverse.checked) {
      state.question.numValue = randomVal;
      $.progress.innerText = `已练习数字：${stats.total} 个`;
      $.question.innerText = "假名：" + state.question.jp;
      $.kanjiDisplay.innerText = $.showKanji.checked ? state.question.kanji : "";
    } else {
      $.progress.innerText = `已练习数字：${stats.total} 个`;
      $.question.innerText = "数字：" + state.question.cn;
      $.kanjiDisplay.innerText = $.showKanji.checked ? state.question.kanji : "";
    }
    return true;
  },

  check(userAnswer) {
    let isCorrect = false;

    if ($.exerciseReverse.checked) {
      isCorrect = parseInt(userAnswer.replace(/,/g, ''), 10) === state.question.numValue;
    } else {
      isCorrect = userAnswer === state.question.jp;
    }

    if (isCorrect) {
      if (this.currentLevel === this.digitLevels.length - 1) {
        this._roundOver = true;
      } else {
        this.currentLevel++;
      }
    }
    return isCorrect;
  },

  numberToJapanese(num) {
    if (num === 0) return "れい/ゼロ";
    const units = ["", "いち", "に", "さん", "よん", "ご", "ろく", "なな", "はち", "きゅう"];

    function parseFourDigits(n) {
      let res = "";
      let sen = Math.floor(n / 1000);
      if (sen === 1) res += "せん";
      else if (sen === 3) res += "さんぜん";
      else if (sen === 8) res += "はっせん";
      else if (sen > 1) res += units[sen] + "せん";

      let hyaku = Math.floor((n % 1000) / 100);
      if (hyaku === 1) res += "ひゃく";
      else if (hyaku === 3) res += "さんびゃく";
      else if (hyaku === 6) res += "ろっぴゃく";
      else if (hyaku === 8) res += "はっぴゃく";
      else if (hyaku > 1) res += units[hyaku] + "ひゃく";

      let juu = Math.floor((n % 100) / 10);
      if (juu === 1) res += "じゅう";
      else if (juu > 1) res += units[juu] + "じゅう";

      res += units[n % 10];
      return res;
    }

    let result = "";
    let okuPart = Math.floor(num / 100000000);
    if (okuPart > 0) result += (okuPart === 1 ? "いちおく" : units[okuPart] + "おく");

    let manPart = Math.floor((num % 100000000) / 10000);
    if (manPart > 0) {
      if (manPart === 1) result += "いちまん";
      else result += parseFourDigits(manPart) + "まん";
    }

    let rest = num % 10000;
    if (rest > 0) result += parseFourDigits(rest);

    return result;
  }
};


// ========== 模式切换与核心交互 ==========
function getActiveEngine() {
  return state.mode === 'vocab' ? StaticWordEngine : DynamicExerciseEngine;
}

function switchMode(mode) {
  state.mode = mode;
  state.question = null;
  resetStats();

  $.tabVocab.classList.toggle("active", mode === 'vocab');
  $.tabExercise.classList.toggle("active", mode === 'exercise');
  $.vocabControls.style.display = mode === 'vocab' ? "" : "none";
  $.exerciseControls.style.display = mode === 'exercise' ? "" : "none";

  $.nextBtn.innerText = "开始";
  $.question.innerText = "模式已切换，请点击底部【开始】按钮";
  $.kanjiDisplay.innerText = "";
  $.progress.innerText = "";
  $.answer.value = "";
  $.result.innerText = "";
  updatePlaceholder();
}

let _locked = false;

function handleNext() {
  if (_locked) return;
  const engine = getActiveEngine();

  if (!state.question) {
    const initSuccess = engine.init();
    if (!initSuccess) {
      $.question.innerText = "⚠️ 请先选择需要听写的课时！";
      $.nextBtn.innerText = "开始";
      return;
    }
  }

  $.nextBtn.innerText = "跳过";
  updatePlaceholder();

  if (!engine.next()) {
    triggerCelebration();
    return;
  }

  $.answer.value = "";
  $.result.innerText = "";

  setTimeout(() => $.answer.focus(), 10);
}

function triggerCelebration() {
  state.question = null;
  const summary = $.stats.innerText;
  $.question.innerText = "🎉 本轮完成！";
  $.kanjiDisplay.innerText = "";
  $.progress.innerText = "";
  $.nextBtn.innerText = "再来一轮";
  $.answer.value = "";
  $.result.innerHTML = `<span style="font-size:20px;">${summary}</span>`;
  confetti({ particleCount: 150, spread: 80, origin: { y: 0.6 } });
}

function checkAnswer() {
  if (!state.question || _locked) return;

  const userAnswer = $.answer.value.trim().toLowerCase();
  const engine = getActiveEngine();
  const isCorrect = engine.check(userAnswer);

  if (!state.question._answered) {
    state.question._answered = true;
    stats.total++;
    if (isCorrect) {
      stats.correct++;
    } else {
      stats.incorrect++;
    }
    updateStatsDisplay();
  }

  if (isCorrect) {
    _locked = true;
    $.result.innerHTML = '<div class="result-correct">✅ 正确！<span class="result-hint">即将进入下一题...</span></div>';
    setTimeout(() => { _locked = false; handleNext(); }, 800);
  } else {
    const correctAnswer = state.question.numValue !== undefined ? state.question.cn : state.question.jp;
    $.result.innerHTML = `<div class="result-wrong">❌ 错误<span class="result-answer">${correctAnswer}</span></div>`;
    $.answer.focus();
  }

  setTimeout(() => $.result.scrollIntoView({ behavior: 'smooth', block: 'nearest' }), 50);
}


